
package Clases;

/**
 * Metodo para el formmato del correo electronico
 * @author samuel
 */
public class Correo {
   ;
    private String usuarioCorre;
    private String contraC;
    private String RutaArchivo;
    private String NombreArchivo;
    private String destinos;
    private String asuntos;
    private String mensajes;

    public String getUsuarioCorre() {
        return usuarioCorre;
    }

    public void setUsuarioCorre(String usuarioCorre) {
        this.usuarioCorre = usuarioCorre;
    }

    public String getContraC() {
        return contraC;
    }

    public void setContraC(String contraC) {
        this.contraC = contraC;
    }

    public String getRutaArchivo() {
        return RutaArchivo;
    }

    public void setRutaArchivo(String RutaArchivo) {
        this.RutaArchivo = RutaArchivo;
    }

    public String getNombreArchivo() {
        return NombreArchivo;
    }

    public void setNombreArchivo(String NombreArchivo) {
        this.NombreArchivo = NombreArchivo;
    }

    public String getDestinos() {
        return destinos;
    }

    public void setDestinos(String destinos) {
        this.destinos = destinos;
    }

    public String getAsuntos() {
        return asuntos;
    }

    public void setAsuntos(String asuntos) {
        this.asuntos = asuntos;
    }

    public String getMensajes() {
        return mensajes;
    }

    public void setMensajes(String mensajes) {
        this.mensajes = mensajes;
    }
    
    
    
}
