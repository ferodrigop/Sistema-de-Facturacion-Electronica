    private void btnAgregaralafacturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregaralafacturaMouseClicked
        if (jComboBoxTipoFactura.getSelectedItem().toString().equals("Seleccione el tipo de factura")
                || jComboBoxCliente.getSelectedItem().toString().equals("Seleccione a un cliente")
                || jComboBoxTipoPago.getSelectedItem().toString().equals("Seleccione una forma de pago")
                || txtCodigoBarras.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campos vacios");
        } else {
            DefaultTableModel model = (DefaultTableModel) Tabla_Factura.getModel();
            Double s1 = Double.parseDouble(txtPrecioUnitario.getText());
            Integer stockactivo = Integer.parseInt(lblstockactivo.getText());
            Integer s2 = Integer.parseInt(txtCantidad.getText());
            if (stockactivo < s2) {
                JOptionPane.showMessageDialog(null, "No puede incluir en la factura inventario inexistente", "Atención!", JOptionPane.WARNING_MESSAGE);
            } else {
                Double s3 = Double.parseDouble(txtIva.getText());
                String s4 = txtCodigoBarras.getText();

                Double precio = s1;

                Double subtotal = precio * s2;
                df.format(subtotal);

                txtsubtotal.setText(String.valueOf(subtotal));

                Double iva = s3;
                Double resultadoiva = subtotal * iva;
                df.format(resultadoiva);

                txtIvaTotal.setText(String.valueOf(resultadoiva));

                df.format(resultadoiva);
                Double resultadototal = resultadoiva + subtotal;

                model.addRow(new Object[]{txtCantidad.getText(), txtProducto.getText(), txtPrecioUnitario.getText(), df.format(resultadototal)});
                limpiarcampos();
                getsum();

                Clases.Mto_Factura obj = new Clases.Mto_Factura();
                obj.setCodigo_barras(s4);
                obj.setStock_activo(s2);
                if (obj.RestarStock()) {
                    JOptionPane.showMessageDialog(this, "Existencias restadas");
                } else {
                    JOptionPane.showMessageDialog(this, "Error al restar las existencias");
                }
            }
        }
    }//GEN-LAST:event_btnAgregaralafacturaMouseClicked

    private void btnAgregarfacturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarfacturaMouseClicked
        if (jComboBoxTipoFactura.getSelectedItem().toString().equals("Seleccione el tipo de factura")
                || jComboBoxCliente.getSelectedItem().toString().equals("Seleccione a un cliente")
                || jComboBoxTipoPago.getSelectedItem().toString().equals("Seleccione una forma de pago")
                || txtCodigoBarras.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campos vacios");
        }else if (txtTotal.getText().equals("") ){
                JOptionPane.showMessageDialog(this, "La factura no puede tener un total vacio");
                } else {
            Clases.Mto_Factura obj = new Clases.Mto_Factura();
            obj.setFecha_factura(txtFecha.getText());
            obj.setId_tipo_factura(Clases.SQL.getExecuteInt("select id_tipo_factura from tipo_factura where tipo_factura = ?", jComboBoxTipoFactura.getSelectedItem().toString()));
            obj.setId_tpago(Clases.SQL.getExecuteInt("select id_tpago from tipo_pago where tipo_pago = ?", jComboBoxTipoPago.getSelectedItem().toString()));
            obj.setId_cliente(Clases.SQL.getExecuteInt("select id_cliente from cliente where nombre_cliente = ?", jComboBoxCliente.getSelectedItem().toString()));
            obj.setMonto(Double.parseDouble(txtsubtotal.getText()));
            obj.setImpuesto_factura(Double.parseDouble(txtIva.getText()));
            obj.setMonto_con_imp(Double.parseDouble(txtIvaTotal.getText()));
            obj.setMonto_total(Double.parseDouble(txtTotal.getText()));
            if (obj.InsertarFactura()) {
                JOptionPane.showMessageDialog(this, "Factura Creada con Exito");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Error al Crear la Factura");
            }
            limpiarcampos();
        }       
    }//GEN-LAST:event_btnAgregarfacturaMouseClicked

    private void btnEliminardelafacturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminardelafacturaMouseClicked
        if (txtCodigoBarras.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Inserte o Escane un codigo de barras");
        } else {
            DefaultTableModel model = (DefaultTableModel) Tabla_Factura.getModel();
            model.removeRow(Tabla_Factura.getSelectedRow());
            Integer s2 = Integer.parseInt(txtCantidad.getText());
            String s4 = txtCodigoBarras.getText();
            Clases.Mto_Factura obj = new Clases.Mto_Factura();
            obj.setCodigo_barras(s4);
            obj.setStock_activo(s2);
            if (obj.SumarStock()) {
                JOptionPane.showMessageDialog(this, "Existencias restauradas");
            } else {
                JOptionPane.showMessageDialog(this, "Error al restaurar las existencias");
            }
            limpiarcampos();
            getsum();
            Double totalf = Double.parseDouble(txtTotal.getText());
            if (totalf == 0.00) {
                txtIvaTotal.setText("");
                txtsubtotal.setText("");
            }
        }
    }//GEN-LAST:event_btnEliminardelafacturaMouseClicked

    private void Tabla_FacturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tabla_FacturaMouseClicked
        try {
            int row = Tabla_Factura.getSelectedRow();
            String Cantidad = (Tabla_Factura.getModel().getValueAt(row, 0).toString());
            txtCantidad.setText(Cantidad);
            String Table_click = (Tabla_Factura.getModel().getValueAt(row, 1).toString());
            String sql = "select codigo_barras as \"Codigo de Barras\", nombre_producto as \"Producto\", precio_venta_producto as \"Precio\", stock_activo as \"Stock\" from producto where nombre_producto = '" + Table_click + "' ";
            PreparedStatement ps = cn.Conectar().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                String add1 = rs.getString("Codigo de Barras");
                txtCodigoBarras.setText(add1);
                String add2 = rs.getString("Producto");
                txtProducto.setText(add2);
                String add3 = rs.getString("Precio");
                txtPrecioUnitario.setText(add3);
                String add4 = rs.getString("Stock");
                lblstockactivo.setText(add4);
            }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_Tabla_FacturaMouseClicked

