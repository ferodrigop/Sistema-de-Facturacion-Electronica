    private void setJTexFieldChanged(JTextField txt) {
        DocumentListener documentListener = new DocumentListener() {

            public void changedUpdate(DocumentEvent documentEvent) {
                printIt(documentEvent);
            }

            public void insertUpdate(DocumentEvent documentEvent) {
                printIt(documentEvent);
            }

            public void removeUpdate(DocumentEvent documentEvent) {
                printIt(documentEvent);
            }
        };
        txt.getDocument().addDocumentListener(documentListener);

    }

    private void printIt(DocumentEvent documentEvent) {
        DocumentEvent.EventType type = documentEvent.getType();

        if (type.equals(DocumentEvent.EventType.CHANGE)) {
            try {
                String codigo = txtCodigoBarras.getText();
                String sql = "select nombre_producto as \"Producto\", stock_activo as \"Stock\", precio_venta_producto as \"Precio\" from producto where codigo_barras = '" + codigo + "' ";
                PreparedStatement ps = cn.Conectar().prepareStatement(sql);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    String add1 = rs.getString("Producto");
                    txtProducto.setText(add1);
                    String add2 = rs.getString("Stock");
                    lblstockactivo.setText(add2);
                    String add3 = rs.getString("Precio");
                    txtPrecioUnitario.setText(add3);
                }
            } catch (Exception e) {
                System.out.println(e.toString());
            }
        } else if (type.equals(DocumentEvent.EventType.INSERT)) {
            try {
                String codigo = txtCodigoBarras.getText();
                String sql = "select nombre_producto as \"Producto\", stock_activo as \"Stock\", precio_venta_producto as \"Precio\" from producto where codigo_barras = '" + codigo + "' ";
                PreparedStatement ps = cn.Conectar().prepareStatement(sql);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    String add1 = rs.getString("Producto");
                    txtProducto.setText(add1);
                    String add2 = rs.getString("Stock");
                    lblstockactivo.setText(add2);
                    String add3 = rs.getString("Precio");
                    txtPrecioUnitario.setText(add3);
                }
            } catch (Exception e) {
                System.out.println(e.toString());
            }
        } else if (type.equals(DocumentEvent.EventType.REMOVE)) {
            txtProducto.setText("");
            txtPrecioUnitario.setText("");
        }
    }

